#git commands
## to clone repo --> git clone [git url]
## to connect to remote repo --> git remote add [repo directory] [git url]
## to pull git content --> git pull
## to add file or directory --> git add [file or directory name]
## to commit to git -->  git commit -m [commit message]
## to push to remote -->  git push origin [branch default: master] 
